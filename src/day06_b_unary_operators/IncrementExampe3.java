package day06_b_unary_operators;

public class IncrementExampe3 {
    public static void main(String[] args) {
        int i = 5;
        int z = i++; // z= 5 | i = 6

        System.out.println(z);
        System.out.println(i);


        int a = 10;
        int b = a+ 1;
        System.out.println(a); //10
        System.out.println(b); //11


    }
}

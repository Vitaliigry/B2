package day06_a_arithmetic_operators;

public class DifferentTypes {

    public static void main(String[] args) {

        // both of them is int
        System.out.println(10 - 5);
        System.out.println(10 * 5);
        System.out.println();


        //one side is double and another side is it ---- double and int --- double
        System.out.println(10 / 5);
    }
}
